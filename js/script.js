


function clickMenu() {
  
  if (itens.style.display == 'block') {
      itens.style.display = 'none';
  } else {
    itens.style.display = 'block';

  }
      
}



function blockchainMenu() {
  
  if (itens.blockchain.display == 'block') {
      itens.blockchain.display = 'none';
  } else {
    itens.blockchain.display = 'block';

  }
      
}

